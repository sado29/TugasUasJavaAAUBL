package com.film.javabeans;

public class Customer {
    private int id;
    private String kodeCustomer;
    private String namaCustomer;
    private String alamatCustomer;
    private String teleponCustomer;
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
//////////////////////////////////////////////////////
    public String getKodeCustomer() {
        return kodeCustomer;
    }

    public void setKodeCustomer(String kodeCustomer) {
        this.kodeCustomer = kodeCustomer;
    }
//////////////////////////////////////////////////////////
    public String getNamaCustomer() {
        return namaCustomer;
    }

    public void setNamaCustomer(String namaCustomer) {
        this.namaCustomer = namaCustomer;
    }
/////////////////////////////////////////////////////////////
    public String getAlamatCustomer() {
        return alamatCustomer;
    }

    public void setAlamatCustomer(String alamatCustomer) {
        this.alamatCustomer = alamatCustomer;
    }
/////////////////////////////////////////////////////////////
    public String getTeleponCustomer() {
        return teleponCustomer;
    }

    public void setTeleponCustomer(String teleponCustomer) {
        this.teleponCustomer = teleponCustomer;
    }
/////////////////////////////////////////////////////////////
}   